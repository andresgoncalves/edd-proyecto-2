    public String getSummaries(){
        String summariesString="";
        ListNode<Summary> summaryNode=summaries.get(author).getFirst();
        while(summaryNode!=null){
            summariesString=summariesString+summaries.get(author).getFirst().getValue().getTitle()+"\n";
            summaryNode=summaries.get(author).getFirst().getNext();
        }
        return summariesString;
    }
