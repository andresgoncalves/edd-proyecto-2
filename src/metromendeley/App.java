    public void registerSummary(Summary summary) {
        /* Agregar resumen a la tabla principal */
        summariesByTitle.set(summary.getTitle(), summary);
        /* Agregar resumen a la tabla indexada por autores */
        for(String author : summary.getAuthors()) {
            List<Summary> authorList = summariesByAuthor.get(author);
            if(authorList == null) {
                authorList = new List<>();
                summariesByAuthor.set(author, authorList);
            }
            authorList.append(summary);
        }
        /* Agregar resumen a la tabla indexada por palabras clave */
        for(String keyword : summary.getKeywords()) {
            List<Summary> keywordList = summariesByKeyword.get(keyword);
            if(keywordList == null) {
                keywordList = new List<>();
                summariesByKeyword.set(keyword, keywordList);
            }
            keywordList.append(summary);
        }
    }
    public void loadTxt(File fileLoad){
        try{
            Summary summaryRegister=Database.readSummaryTxt(fileLoad);
            summarySaved=Database.readSummaryTxt(fileLoad);
            
            registerSummary(summaryRegister);
            JOptionPane.showMessageDialog(null, "Carga Exitosa");
            showOptions();
        } catch(DuplicateKeyException d){
            JOptionPane.showMessageDialog(welcomePanel, "Este resumen ya fue cargado");
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "No se encontrï¿½ el archivo", "Archivo no encontrado", JOptionPane.ERROR_MESSAGE);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "No se pudo leer el archivo", "Error de lectura", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex, "Error de formato", JOptionPane.ERROR_MESSAGE);
        } 
    }
    
